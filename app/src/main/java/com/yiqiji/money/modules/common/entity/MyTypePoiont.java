package com.yiqiji.money.modules.common.entity;

public class MyTypePoiont {
	private float x;
	private float y;

	public MyTypePoiont(float x, float y) {
		this.x = x;
		this.y = y;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

}
